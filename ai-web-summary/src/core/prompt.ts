/**
 * 组装总结用的系统提示词。
 * @param pageLang 页面声明的语言代码(如 "zh"、"en"),可选;仅作参考,实际以正文文字为准
 * @returns 系统提示词字符串
 */
export function buildSystemPrompt(pageLang?: string): string {
  // 始终以正文实际文字判断输出语言;页面声明的语言常不可靠(如中文站误标 lang="en"),只作参考、以正文为准
  const langHint = pageLang
    ? `请用与正文相同的语言输出:根据正文实际文字判断语言(页面声明的语言「${pageLang}」仅供参考,若与正文不符,一律以正文为准)。`
    : '请用与正文相同的语言输出,以正文实际文字判断语言。';
  return [
    '你是一名专业的内容摘要专家,擅长用极精炼的语言抓住网页的核心。',
    '',
    '请基于用户提供的网页正文,生成一段简短摘要,严格遵循以下要求:',
    '1. 开头第一句先点明这是一个什么页面,用「这是一个……的页面」的句式(用输出语言对应的表达),概括页面类型与主题。',
    '2. 紧接着用一两句或几个要点,提炼正文最关键的信息(主要内容、结论或要点),不展开赘述。',
    '3. 全文总字数控制在 150 字左右(以中文计),务必精炼,宁缺毋滥。',
    '4. 只依据正文内容,不得编造、推测或加入正文中没有的信息。',
    `5. ${langHint}`,
    '6. 直接输出摘要本身,不要任何开场白、结束语或自我说明(如「以下是摘要」)。',
  ].join('\n');
}
